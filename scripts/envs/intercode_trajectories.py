# ---------------------------------------------------------------------------
# Row filtering / cleaning
# ---------------------------------------------------------------------------

_PY_MARKERS = ("def ", "import ", "print(", "class ", "return ", "```python", "self.")
_MUTATION_PREFIXES = (
    "touch ", "mkdir ", "cp ", "mv ", "rm ", "chmod ", "chown ", "ln ",
)
_FENCE_RE = re.compile(r"^```(?:bash|sh|shell)?\s*\n(.*?)\n?```\s*$", re.DOTALL)

_THOUGHT_TEMPLATES = (
    "I can use the `{tool}` command to accomplish this.",
    "The `{tool}` command should handle this task.",
    "I should use `{tool}` here.",
    "Using `{tool}` is the most direct way to do this.",
)


def _strip_fence(text: str) -> str:
    m = _FENCE_RE.match(text.strip())
    return m.group(1).strip() if m else text.strip()


def _is_bash_like(cmd: str) -> bool:
    """Single-line, plausibly-bash command (filters out the bigcode Python half)."""
    if not cmd or "\n" in cmd:
        return False
    if len(cmd) > 600:
        return False
    lowered = cmd.lstrip()
    if any(m in lowered for m in _PY_MARKERS):
        return False
    return True


def _is_empty_obs_mutation(cmd: str) -> bool:
    """Commands whose successful execution prints nothing — safe for a truthful
    empty-Observation submit example."""
    stripped = cmd.lstrip()
    if stripped.startswith(_MUTATION_PREFIXES):
        # exclude pipes/command-substitution that might print
        return "|" not in stripped and "$(" not in stripped
    if stripped.startswith("echo ") and (">" in stripped):
        return "|" not in stripped
    if stripped.startswith("sed ") and " -i" in stripped:
        return "|" not in stripped
    return False


def _make_thought(cmd: str, rng: random.Random) -> str:
    tool = cmd.lstrip().split(" ", 1)[0] or "bash"
    return rng.choice(_THOUGHT_TEMPLATES).format(tool=tool)


# ---------------------------------------------------------------------------
# Whitelisted dataset discovery (MINER_DATASETS_DIR / MINER_DATASETS)
# ---------------------------------------------------------------------------

def _find_intercode_dataset_file() -> Path | None:
    """Locate the intercode jsonl inside the validator-mounted miner datasets."""
    root = os.environ.get("MINER_DATASETS_DIR")
    if not root:
        return None
    root_path = Path(root)
    names = [n for n in os.environ.get("MINER_DATASETS", "").split(",") if n.strip()]
    candidates = [root_path / n.strip() for n in names] if names else []
    # Fallback: scan all children when MINER_DATASETS unset
    if not candidates and root_path.is_dir():
        candidates = [p for p in root_path.iterdir() if p.is_dir()]
    for cand in candidates:
        if "intercode" not in cand.name.lower():
            continue
        jsonls = sorted(cand.rglob("*.jsonl"))
        if jsonls:
            return jsonls[0]
        jsons = [p for p in sorted(cand.rglob("*.json")) if "meta" not in p.name]
        if jsons:
            return jsons[0]
    return None


def _load_rows(path: Path) -> list[dict]:
    rows: list[dict] = []
    text = path.read_text(encoding="utf-8", errors="replace")
    if path.suffix == ".jsonl":
        for line in text.splitlines():
            line = line.strip()
            if not line:
                continue
            try:
                rows.append(json.loads(line))
            except json.JSONDecodeError:
                continue
    else:
        data = json.loads(text)
        rows = data if isinstance(data, list) else data.get("data", [])
    return rows


# ---------------------------------------------------------------------------
# Public entry — called by envs/generate_trajectories.py intercode branch
# ---------------------------------------------------------------------------

def build_intercode_dataset(
    output_path: str,
    num_examples: int = 12_000,
    seed: int = 42,
    max_time_seconds: int = 0,
    submit_example_ratio: float = 0.35,
) -> None:
    """Build the ReAct-format SFT DatasetDict and save_to_disk(output_path)."""
    t0 = time.time()
    rng = random.Random(seed)

    src = _find_intercode_dataset_file()
    if src is None:
        raise RuntimeError(
            "InterCode SFT source not found. Ensure your miner endpoint returns "
            f'requested_datasets=["{WHITELIST_DATASET_ID}"] so the validator '
            "mounts it under MINER_DATASETS_DIR (env vars MINER_DATASETS_DIR / "
            "MINER_DATASETS)."
        )
    rows = _load_rows(src)
    print(f"[intercode_trajectories] Loaded {len(rows)} rows from {src}", flush=True)

    keep_non_bash = os.environ.get("INTERCODE_KEEP_NON_BASH", "0") == "1"
    conversations: list[list[dict]] = []
    n_submit = 0
    for row in rows:
        if max_time_seconds and (time.time() - t0) >= max_time_seconds:
            print("[intercode_trajectories] TIME LIMIT reached — stopping early.", flush=True)
            break
        if len(conversations) >= num_examples:
            break
        instruction = str(row.get("instruction", "")).strip()
        response = _strip_fence(str(row.get("response", "")))
        if not instruction or not response:
            continue
        if not _is_bash_like(response) and not keep_non_bash:
            continue

        thought = _make_thought(response, rng)
        base_user = (
            REACT_INIT_MSG + DEMO_BASH_REACT + f"Question: {instruction}\nThought 1:"
        )
        turn1_assistant = f" {thought}\nAction 1: execute[{response}]"
        conversations.append(
            [
                {"role": "user", "content": base_user},
                {"role": "assistant", "content": turn1_assistant},
            ]
        )

        # Truthful submit follow-up only when empty observation is realistic.
        if _is_empty_obs_mutation(response) and rng.random() < submit_example_ratio:
            turn2_user = (
                base_user
                + f" {thought}\nAction 1: execute[{response}]\n"
                + "Observation 1: \nThought 2:"
            )
            turn2_assistant = (
                " The command executed successfully and the task is complete. "
                "I will submit.\nAction 2: submit"
            )
            conversations.append(
                [
                    {"role": "user", "content": turn2_user},
                    {"role": "assistant", "content": turn2_assistant},
                ]
            )
            n_submit += 1

    if not conversations:
        raise RuntimeError(
            f"No usable bash rows extracted from {src} "
            f"(rows={len(rows)}, keep_non_bash={keep_non_bash})."
        )

    rng.shuffle(conversations)
    dataset = Dataset.from_list([{"messages": c} for c in conversations])
    splits = dataset.train_test_split(test_size=VALIDATION_RATIO, seed=seed)
    dd = DatasetDict({"train": splits["train"], "validation": splits["test"]})
    dd.save_to_disk(output_path)
    print(
        f"[intercode_trajectories] Saved {len(conversations)} examples "
        f"({n_submit} submit-turn) -> {output_path} "
        f"in {time.time() - t0:.1f}s",
        flush=True,
    )
