"""InterCode SFT dataset builder — NL2Bash ReAct format matching eval_intercode.py.

Unlike the OpenSpiel games, InterCode has NO env-server sidecar during training
(trainer/image_manager.py `_select_training_env_server_name` skips intercode),
so expert trajectories cannot be generated by playing episodes. Instead we
build SFT data from the WHITELISTED miner-requested dataset:

    gradients-io-tournaments/intercode_bigcode_combined_12k
    (rows: {"instruction": ..., "response": ...};
     6k Tellina NL2Bash-derived + 6k bigcode self-oss-instruct)

IMPORTANT — miner endpoint must request it:
    requested_datasets=["gradients-io-tournaments/intercode_bigcode_combined_12k", ...]
(max 2 datasets total; see core/whitelisted_sft_datasets.json upstream).
Without the mount, this builder raises and text_trainer drops intercode from
the multi-env task (same graceful-drop path as before).

Eval contract being matched (validator/evaluation/eval_intercode.py):
  - Single user message: REACT_INIT_MSG + DEMO_BASH_REACT + "Question: {q}\n"
    then the loop appends "Thought {t}:" and samples with
    stop=["\nObservation {t}:"].
  - Model must emit: "{thought}\nAction {t}: execute[{bash}]" or
    "...\nAction {t}: submit".
  - Action parser: re.compile(r"execute\[(.*)\]", re.DOTALL); anything else →
    "Error executing query" observation, reward 0.
  - Per-turn generation is cut at "\nObservation {t}:" so the assistant target
    in SFT must contain EXACTLY one Thought/Action pair per example.

SFT example shapes produced here:
  Turn-1 (every kept row):
    user:      REACT_INIT_MSG + DEMO_BASH_REACT + "Question: {instruction}\nThought 1:"
    assistant: " {thought}\nAction 1: execute[{response}]"
  Turn-2 submit (only for mutation commands where an EMPTY observation is
  truthful — touch/mkdir/cp/mv/rm/chmod/ln/sed -i/echo-redirect):
    user:      <turn-1 user> + " {thought}\nAction 1: execute[{response}]\n"
               + "Observation 1: \nThought 2:"
    assistant: " The command executed successfully and the task is complete. I will submit.\nAction 2: submit"

Rows whose response is not a single-line bash command (the bigcode Python half)
are filtered out by default; set INTERCODE_KEEP_NON_BASH=1 to keep them as
plain instruction-following pairs (NOT recommended for env tournaments).
"""

from __future__ import annotations

import json
import os
import random
import re
import time
from pathlib import Path

from datasets import Dataset, DatasetDict


VALIDATION_RATIO = 0.01  # mirror envs/generate_trajectories.py

WHITELIST_DATASET_ID = "gradients-io-tournaments/intercode_bigcode_combined_12k"

# ---------------------------------------------------------------------------
# ReAct prompt — VERBATIM from validator/evaluation/eval_intercode.py
# (inlined from princeton-nlp/intercode experiments/utils/prompts.py).
# Do not edit: training prompt must byte-match the eval prompt.
# ---------------------------------------------------------------------------

